import fs from "node:fs";
import path from "node:path";
import { pathToFileURL } from "node:url";

const inputPath = path.resolve("data/reviewed-game-artifacts.json");
const outputPath = path.resolve("public/game-artifacts.generated.js");

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  main();
}

export function main() {
  const artifacts = readReviewedArtifacts(inputPath);
  writeGeneratedArtifacts(artifacts, outputPath);
  console.log(`Reviewed artifacts written to ${path.relative(process.cwd(), outputPath)}`);
}

export function readReviewedArtifacts(filePath) {
  const artifacts = JSON.parse(fs.readFileSync(filePath, "utf8"));
  validateReviewedArtifacts(artifacts);
  return artifacts;
}

export function validateReviewedArtifacts(artifacts) {
  const failures = [];
  if (artifacts.schemaVersion !== 1) failures.push("schemaVersion must be 1.");
  if (!Array.isArray(artifacts.cases)) failures.push("cases must be an array.");
  if (!Array.isArray(artifacts.doctrineCards)) failures.push("doctrineCards must be an array.");
  if (!Array.isArray(artifacts.sourceCards)) failures.push("sourceCards must be an array.");

  for (const c of artifacts.cases || []) {
    requireReviewed(c, `case ${c.id || "<missing>"}`, failures);
    for (const key of ["id", "title", "type", "summary", "forumState", "court", "eventState"]) {
      if (!c[key]) failures.push(`${c.id || "<missing>"} missing ${key}.`);
    }
    if (!Array.isArray(c.sourceRefs) || !c.sourceRefs.length) failures.push(`${c.id} needs sourceRefs.`);
    if (!Array.isArray(c.topics) || !c.topics.length) failures.push(`${c.id} needs topics.`);
    if (!Array.isArray(c.defendants) || !c.defendants.length) failures.push(`${c.id} needs defendants.`);
    if (!Array.isArray(c.evidence) || !c.evidence.length) failures.push(`${c.id} needs evidence.`);
    if (!Number.isFinite(c.amount)) failures.push(`${c.id} needs numeric amount.`);
    if (typeof c.federalQuestion !== "boolean") failures.push(`${c.id} needs federalQuestion boolean.`);
    if (typeof c.serviceProper !== "boolean") failures.push(`${c.id} needs serviceProper boolean.`);
  }

  for (const card of artifacts.doctrineCards || []) {
    requireReviewed(card, `doctrine card ${card.id || "<missing>"}`, failures);
    if (!card.title || !card.summary || !card.topic) failures.push(`${card.id || "<missing>"} doctrine card is incomplete.`);
    if (!Array.isArray(card.sourceRefs) || !card.sourceRefs.length) failures.push(`${card.id} needs sourceRefs.`);
  }

  for (const source of artifacts.sourceCards || []) {
    if (!source.label || !source.href || !source.note) failures.push(`${source.label || "<missing>"} source card is incomplete.`);
    if (source.href && !source.href.startsWith("https://")) failures.push(`${source.label} must use an HTTPS href.`);
  }

  ensureUnique((artifacts.cases || []).map((item) => item.id), "case ids", failures);
  ensureUnique((artifacts.doctrineCards || []).map((item) => item.id), "doctrine card ids", failures);

  if (failures.length) {
    throw new Error(`Reviewed artifact validation failed:\n- ${failures.join("\n- ")}`);
  }
}

export function writeGeneratedArtifacts(artifacts, filePath) {
  fs.writeFileSync(filePath, buildGeneratedArtifactsModule(artifacts));
}

export function buildGeneratedArtifactsModule(artifacts) {
  return `// Generated by scripts/build-reviewed-artifacts.js. Only reviewed artifacts are compiled into the game.
export const REVIEWED_GAME_ARTIFACTS = ${JSON.stringify(artifacts, null, 2)};

export const REVIEWED_CASES = REVIEWED_GAME_ARTIFACTS.cases;
export const REVIEWED_DOCTRINE_CARDS = REVIEWED_GAME_ARTIFACTS.doctrineCards;
export const REVIEWED_SOURCE_CARDS = REVIEWED_GAME_ARTIFACTS.sourceCards;
`;
}

function requireReviewed(item, label, failures) {
  if (item.reviewStatus !== "reviewed") {
    failures.push(`${label} must have reviewStatus reviewed.`);
  }
}

function ensureUnique(values, label, failures) {
  const seen = new Set();
  for (const value of values) {
    if (!value) continue;
    if (seen.has(value)) failures.push(`Duplicate ${label}: ${value}.`);
    seen.add(value);
  }
}
